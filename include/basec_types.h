/**
 * @file basec_types.h
 * @brief Types for basec
 */

#ifndef BASEC_TYPES_H
#define BASEC_TYPES_H

#include <stdint.h>
#include <stdbool.h>

// Unsigned integers
typedef uint8_t  u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

// Signed integers
typedef int8_t  i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

// Floating point numbers
typedef float  f32;
typedef double f64;

// Characters
typedef char c8;

// Strings
typedef char* c_str;

#endif
